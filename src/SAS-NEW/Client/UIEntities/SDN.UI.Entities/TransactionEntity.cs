using SDN.Common;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SDN.UI.Entities
{
    public class TransactionEntity : ViewModelBase
    {
        private int _ID;
        private string _TransactionName;
        private bool? _IsChecked;
        private string _StartDate;
        private string _EndDate;
        private DateTime? _EndDateDate;
        private DateTime? _StartDateDate;
    

        public int ID
        {
            get
            {
                return _ID;
            }
            set
            {
                _ID = value;
                OnPropertyChanged("ID");
            }
        }
        public string TransactionName
        {
            get
            {
                return _TransactionName;
            }
            set
            {
                _TransactionName = value;
                OnPropertyChanged("TransactionName");
            }
        }
        public bool? IsChecked
        {
            get
            {
                return _IsChecked;
            }
            set
            {
                _IsChecked = value;
                OnPropertyChanged("IsChecked");
            }
        }
        public string StartDate
        {
            get
            {
                return _StartDate;
            }
            set
            {
                _StartDate = value;
                OnPropertyChanged("StartDate");
            }
        }
        public string EndDate
        {
            get
            {
                return _EndDate;
            }
            set
            {
                _EndDate = value;
                OnPropertyChanged("EndDate");
            }
        }
        public DateTime? EndDateDate
        {
            get
            {
                return _EndDateDate;
            }
            set
            {
                _EndDateDate = value;
                OnPropertyChanged("EndDateDate");
            }
        }
        public DateTime? StartDateDate
        {
            get
            {
                return _StartDateDate;
            }
            set
            {
                _StartDateDate = value;
                OnPropertyChanged("StartDateDate");
            }
        }
       
    }
}
