using SDN.Common;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SDN.UI.Entities   
{
    public class RestoreDataEntity : ViewModelBase
    {
        #region "private entity"
        private string _msg;
        #endregion
        #region "public property"
        public string Msg
        {
            get
            {
                return _msg;
            }
            set
            {
                _msg = value;
                OnPropertyChanged("Msg");
            }
        }
        #endregion

    }
}
