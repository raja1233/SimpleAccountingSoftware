using SDN.Common;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SDN.UI.Entities.Export
{
    public class Account_Detail:ViewModelBase
    {
        #region Private Properties
        private string _Account_Name;
        private string _Account_Type;
        private string _Opening_Balance;
        private string _Isinactive;
        #endregion

        #region
        public string Account_Name
        {
            get { return _Account_Name; }

            set
            {
                if (_Account_Name != value)
                {
                    _Account_Name = value;
                    OnPropertyChanged("Account_Name");

                }
            }
        }
        public string Account_Type
        {
            get { return _Account_Type; }

            set
            {
                if (_Account_Type != value)
                {
                    _Account_Type = value;
                    OnPropertyChanged("Account_Type");

                }
            }
        }
        public string Opening_Balance
        {
            get { return _Opening_Balance; }

            set
            {
                if (_Opening_Balance != value)
                {
                    _Opening_Balance = value;
                    OnPropertyChanged("Opening_Balance");

                }
            }
        }
        public string Isinactive
        {
            get { return _Isinactive; }

            set
            {
                if (_Isinactive != value)
                {
                    _Isinactive = value;
                    OnPropertyChanged("Isinactive");

                }
            }
        }
        #endregion
    }
}
