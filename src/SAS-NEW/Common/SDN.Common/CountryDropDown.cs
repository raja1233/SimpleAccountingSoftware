using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace SDN.Common
{
   public class CountryDropDown
    {
        public string _countryname = string.Empty;
        public string _countrycode = string.Empty;

        public string CountryName
        {
            get
            {
                return _countryname;
            }
            set
            {
                _countryname = value;
            }
        }

        public string CountryCode
        {
            get
            {
                return _countrycode;
            }
            set
            {
                _countrycode = value;
            }
        }
    }
}
